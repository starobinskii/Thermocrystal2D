FLT Vect2D::Abs() const	{ return sqrt(Sqr()); }

//-----------------------------------------------------------------------------------

void Vect2D::SetRand(FLA x_min, FLA y_min, FLA x_max, FLA y_max)
{
	x = rand(x_min, x_max);
	y = rand(y_min, y_max);
}

//-----------------------------------------------------------------------------------

void Vect2D::SetRand(FLA r_max)
{
	FLT rr_max = r_max * r_max;
	do SetRand(-r_max, -r_max, r_max, r_max);
	while (Sqr() > rr_max);
	//x = rand(-r_max, r_max);
	//y = 0;
}

