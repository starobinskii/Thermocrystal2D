#ifndef ___UTIL_H___
#define ___UTIL_H___

#include "def.h"
#include <stdlib.h>

//-----------------------------------------------------------------------------------

static const FLT PI = 3.1415926535897932;
static const FLT DEG = PI / 180;

//-----------------------------------------------------------------------------------
//	RAND_MAX = 32767 = 2^15 - 1
//-----------------------------------------------------------------------------------

inline FLT rand(FLA max)			//	Random FLT value from 0 to max
{ 
	return max * (FLT)rand() / RAND_MAX; 
}

//-----------------------------------------------------------------------------------

inline FLT rand(FLA min, FLA max)	//	Random FLT value from min to max
{ 
	return min + (max - min) * (FLT)rand() / RAND_MAX; 
}

//-----------------------------------------------------------------------------------

inline LONG rand(SHORT max)		//	Random integer value from 0 to (max - 1)
{ 
	return max * rand() >> 15;	//	x >> 15 = x / ((LONG)RAND_MAX + 1)
}	

//-----------------------------------------------------------------------------------

template <class T> inline void swap(T& x1, T& x2) { T x = x1; x1 = x2; x2 = x; }

//-----------------------------------------------------------------------------------

template <class T> inline T sqr(T x) { return x * x; }

//-----------------------------------------------------------------------------------

#endif //___UTIL_H___
